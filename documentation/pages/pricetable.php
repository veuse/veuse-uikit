<h1>Pricetables</h1>
<p> With Veuse Pricetable,  the posts are called price-items, and the categories are called Pricetables. In short, price-items are organized in pricetables, just like posts are organized in categories.</p>

<hr>

<h3>Price Items</h3>
<h4>Create a price-item to go in your Pricetable</h4>
<ol>
	<li>Go to <a href="post-new.php?post_type=portfolio" target="blank">Pricetable &raquo; New Price Item.</a> This creates a new price item. </li>
	<li>Enter data for the price-item.</li>
	<li>Assign it to a pricetable (category).</li>
</ol>

<hr>

<h3>Pricetables</h3>
<h4>Pricetable entries are called <b>price-items</b>, and are organized in categories called pricetables.</h4>

<p>How to create a pricetable</p>
<ol>
	<li>Go to <a href="edit-tags.php?taxonomy=pricetable&post_type=priceitem" target="blank">Pricetable &raquo; Pricetable.</a></li>
	<li>Under the heading "Add New Pricetable", enter a name for your pricetable and click on the button <b>Add New Pricetable</b></li>
</ol>

<hr>

<h3>Displaying your pricetable</h3>
		
<p>There are two ways of showing your pricetable on your site. You can either use a shortcode, or insert via a widget ( if you have installed Page Builder plugin by SiteOrigin</p>

<code>[veuse_pricetable pricetable=""]</code>

<h3>Attributes</h3>
<p><b>pricetable:</b> Enter name (slug) of the pricetable you want to insert.</p>
